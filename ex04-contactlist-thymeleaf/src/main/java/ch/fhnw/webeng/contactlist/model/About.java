package ch.fhnw.webeng.contactlist.model;

public class About {

    private int contactAmount;
    private int phoneAmount;
    private int emailAmount;

    public int getContactAmount() {
        return contactAmount;
    }

    public void setContactAmount(int contactAmount) {
        this.contactAmount = contactAmount;
    }

    public int getPhoneAmount() {
        return phoneAmount;
    }

    public void setPhoneAmount(int phoneAmount) {
        this.phoneAmount = phoneAmount;
    }

    public int getEmailAmount() {
        return emailAmount;
    }

    public void setEmailAmount(int emailAmount) {
        this.emailAmount = emailAmount;
    }
}
